import Foundation

/// Spacing tokens
enum MomentraSpacing {
    static let xs: CGFloat = 4        // Minimal spacing
    static let inline: CGFloat = 6    // Inline elements
    static let item: CGFloat = 8      // Between items
    static let section: CGFloat = 12  // Section gaps
    static let cardH: CGFloat = 14    // Card horizontal padding
    static let screenV: CGFloat = 16  // Screen vertical padding
    static let screenH: CGFloat = 18  // Screen horizontal padding
}
